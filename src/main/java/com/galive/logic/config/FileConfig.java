package com.galive.logic.config;

public class FileConfig {

	private String host;
	
	private String path;
	
	private String folder_usr_avatar;
	
	private String folder_meeting_cover;

	public String getHost() {
		return host;
	}

	public void setHost(String host) {
		this.host = host;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public String getFolder_usr_avatar() {
		return folder_usr_avatar;
	}

	public void setFolder_usr_avatar(String folder_usr_avatar) {
		this.folder_usr_avatar = folder_usr_avatar;
	}

	public String getFolder_meeting_cover() {
		return folder_meeting_cover;
	}

	public void setFolder_meeting_cover(String folder_meeting_cover) {
		this.folder_meeting_cover = folder_meeting_cover;
	}
	
	
	
}
